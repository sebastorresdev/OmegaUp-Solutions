PRIMERA SOLUCION
////////////////////////////////////////////////////////////////////////////////
int n = int.Parse(Console.ReadLine());

char[][] m = new char[n][];

for (int i = 0; i < n; i++)
{
    m[i] = new char[n];
}

for (int i = 0; i < n; i++)
{
    for (int j = 0; j < n; j++)
	{   
        if(i == j || j + i == n - 1)
        {
            m[i][j] = '@';
        }
        else
        {
            m[i][j] = ' ';
        }
    }
}

for (int i = 0; i < n; i++)
{
    Console.WriteLine(new string(m[i]));
}

